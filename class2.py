class rectangle:
	length = 0
	breadth = 0
	def __init__(self,x,y):
		self.length=x
		self.breadth=y
	def area(x):
		return x.length*x.breadth
rectangle1=rectangle(20,25)
rectangle2=rectangle(10,15)
rectangle3=rectangle(15,20)
rectangle4=rectangle(25,40)
print("length of rectangle1 is", rectangle1.length)
print("breadth of rectangle1 is", rectangle1.breadth)
print("area of rectangle1 is", rectangle1.area())
